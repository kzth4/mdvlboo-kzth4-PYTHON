# Operasi Matematika

# Penjumlahan "+"
x = 9
v = 7
z = 2
data = x + v + z
print(x,'+',v,'+',z,'=',data)
print()

# Pengurangan "-"
x = 9
v = 7
z = 2
data = x - v - z
print(x,'-',v,'-',z,'=',data)
print()

# Perkalian "*"
x = 9
v = 7
z = 2
data = x * v * z
print(x,'x',v,'x',z,'=',data)
print(type(data))
print() 

# Pembagian "/"
x = 9
v = 7
z = 2
data = x / v / z
print(x,'/',z,'=',data)
print(type(data))
# type otomatis float jika pembagian
print()

# Eksponen (pangkat) "**"
x = 9
v = 7
z = 2
data = x ** v
datax = v ** z
print(x,'^',v,'=',data)
print(v,'^',z,'=',datax)
print()

# Modulus (Hasil Pembagian) "%"
x = 9
v = 7
z = 2
data = v % z
print(v,'%',z,'=',data)
# Sisa pembagian
print()

# Floor Division "//"
x = 9
v = 7
z = 2
data = v // z
print(v,'%',z,'=',data)
#Hasil Bulat Pembagian
print()